namespace DiacriticalMarks
{
    public enum Accent : int
    {
        /// <summary>
        /// The circumflex accent (^).
        /// </summary>
        Circumflex = 0x005E,

        /// <summary>
        /// The grave accent (`).
        /// </summary>
        Grave = 0x0060,

        /// <summary>
        /// The tilde (~).
        /// </summary>
        Tilde = 0x007E,

        /// <summary>
        /// The diaeresis (¨).
        /// </summary>
        Diaeresis = 0x00A8,

        /// <summary>
        /// The macron (¯).
        /// </summary>
        Macron = 0x00AF,

        /// <summary>
        /// The acute accent (´).
        /// </summary>
        Acute = 0x00B4,

        /// <summary>
        /// The cedilla (¸).
        /// </summary>
        Cedilla = 0x00B8,
    }
}
